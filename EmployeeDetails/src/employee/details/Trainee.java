package employee.details;

public class Trainee extends Employee {
	Trainee()
	{
		super();
	}
	Trainee(long id, String name, String address, long phoneno, double basic_salary)
	{
		super();
		setId(id);
		setName(name);
		setAddress(address);
		setPhoneno(phoneno);
		setBasicSalary(basic_salary);
	}
}
